#include<iostream>
using namespace std;
int main(){
    int a = 3, b = 5;
    cout<<(a>b)<<endl;// false = 0
    cout<<(a<b)<<endl; // true = 1
    cout<<(a>=b)<<endl;// false = 0
    cout<<(a<=b)<<endl;//true = 1
    cout<<(a==b)<<endl;//false = 0
    cout<<(a!=b)<<endl;// true = 1

    return 0;
}
